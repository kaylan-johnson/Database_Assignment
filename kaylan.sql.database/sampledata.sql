-- This query will display all the data in all 4 tables at once.

SELECT *
FROM professors p
JOIN students s
ON p.prof_student_id = s.stu_id
JOIN courses c
ON p.prof_course_id = c.course_id
JOIN grades g
ON p.prof_grade_id = g.grade_id
ORDER BY p.prof_name;



